#   数学优化：x + 5z = 100 - 2y解的个数，选择z值不断增大，可以找出总解个数与奇偶的一定规律
#   组合总数为100以内的偶数+95以内的奇数+90以内的偶数+…+5以内的奇数+1，
#   即为： (51+48)+(46+43)+(41+38)+(36+33)+(31+28)+(26+23)+(21+18)+(16+13)+(11+8)+(6+3)+1
m=0
for i in range (0,101,5):
    if(i%2==0):
        m+=(i/2+1)
    else:
        m+=(i+1)/2
t=(51+48)+(46+43)+(41+38)+(36+33)+(31+28)+(26+23)+(21+18)+(16+13)+(11+8)+(6+3)+1
