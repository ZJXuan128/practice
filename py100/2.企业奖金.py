while(1):
    I=int(input("输入当月利润:"))
    if I<10:
        print("奖金为%.3f万元"%(0.1*I))
    elif I<20:
        print("奖金为%.3f万元"%((I-10)*0.075+1))
    elif I<40:
        print("奖金为%.3f万元"%((I-20)*0.05+1.75))
    elif I<60:
        print("奖金为%.3f万元"%((I-40)*0.03+2.75))
    elif I<100:
        print("奖金为%.3f万元"%((I-60)*0.015+3.35))
    else:
        print("奖金为%.3f万元"%((I-100)*0.01+3.95))